<template>
  <i-container>
    <i-table :columns="columns" :rows="shifts" title="Turnos">
      <template v-slot:actions>
        <i-button tooltip="Agregar turno" @click="openAddDialog">
          <i-icon value="add" />
        </i-button>
      </template>
      <template v-slot:expandedRow="{ row }">
        <shift-info :value="row" />
      </template>
      <template v-slot:rowAction="{ row }">
        <i-button tooltip="Editar turno" @click="openEditDialog(row)">
          <i-icon value="edit" />
        </i-button>
      </template>
    </i-table>
    <shift-dialog
      v-if="showDialog"
      show
      :value="selectedRow"
      @close="showDialog = false"
    />
  </i-container>
</template>
<script>
import { mapActions, mapGetters } from "vuex";

import ShiftDialog from "../dialogs/ShiftDialog.vue";

import ShiftInfo from "../info/ShiftInfo";

export default {
  components: {
    ShiftInfo,
    ShiftDialog
  },
  data() {
    return {
      columns: [
        { text: "Nombre", value: "name" },
        { text: "Acciones", value: "actions", sortable: false },
        { text: "", value: "data-table-expand", sortable: false },
      ],
      showDialog: false,
      selectedRow: null,
    };
  },
  computed: {
    ...mapGetters("shifts", ["shifts"]),
  },

  methods: {
    ...mapActions("shifts", ["load"]),

    openEditDialog(row) {
      this.selectedRow = row;
      this.showDialog = true;
    },
    openAddDialog() {
      this.selectedRow = null;
      this.showDialog = true;
    },
  },

  created() {
    this.load();
  },
};
</script>