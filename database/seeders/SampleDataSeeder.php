    public function run()
    {
        collect(self::DATA)->each(function ($data) {
            $this->preProcessData($data);
        });
        collect(self::DATA)->each(function ($data) {
            $this->processData($data);
        });
    }

    private function preProcessData($data)
    {
        $userData = $this->userData($data);
        $email = $userData["email"];
        if (User::where(["email" => $email])->exists()) {
            $this->command->warn("User $email already exists. Deleting");
            $user = User::where(["email" => $email])->first();
            $user->shifts()->delete();
            $user->labels()->delete();
            $user->delete();
            $this->command->warn("User $email deleted!");
        }
    }

    private function processData($data)
    {
        $sector = $data[self::SECTOR] ?? null;
        $userData = $this->userData($data);
        $email = $userData["email"];
        $fullName = $userData["fullName"];
        if (User::where(["email" => $email])->exists()) {
            $this->command->info("User $email already exists");
        } else {
            $this->command->info("Creating user $email...");
            User::create([
                "name" => $fullName,
                "email" => $email,
                "password" => Hash::make(Str::random(8)),
                "is_admin" => $data[self::IS_ADMIN] ?? false,
                "can_login" => $data[self::IS_ADMIN] ?? false
            ]);
            $this->command->info("User $email created!");
        }
        $user = User::where(["email" => $email])->first();
        if ($sector) {
            $this->command->info("Creating label $sector on user $email...");
            $label = Label::firstOrCreate(["text" => $sector]);
            $label->color = self::COLORS[$sector] ?? "#cccccc";
            $label->save();
            if (!$user->labels->contains($label)) {
                $user->labels()->attach($label);
            }
            $this->command->info("Label $sector on user $email created!");
        }
        $this->createShift($user, $data, $sector);
    }

    private function createShift($user, $data, $sector)
    {
        $name = $sector ?? "Único";

        $shift = $user->shifts()->where(["name" => $name])->first();
        if(!$shift) {
            $this->command->info("Creating shift with $name for user $user->email...");
            $shift = $user->shifts()->create([
                "name" => $name,
                "start" => Carbon::now()->startOfMonth(),
                "type" => "week",
                "recurrence" => "weekly"
            ]);
            $this->command->info("Shift $name created on user $user->email...");
            if ($sector) {
                $this->command->info("Creating label $sector on user $user->email...");
                $label = Label::firstOrCreate(["text" => $sector]);
                $label->color = self::COLORS[$sector] ?? "#cccccc";
                $label->save();
                $shift->labels()->attach($label);
                $this->command->info("Label $sector created on shift $name for user $user->email!");
            }
        }
        $this->createShiftEvents($shift, $data, $user);
    }

    private function createShiftEvents($shift, $data, $user)
    {
        $days = [
            [Carbon::MONDAY, $data[self::LUNES_ENTRADA] ?? null, $data[self::LUNES_SALIDA] ?? null],
            [Carbon::TUESDAY, $data[self::MARTES_ENTRADA] ?? null, $data[self::MARTES_SALIDA] ?? null],
            [Carbon::WEDNESDAY, $data[self::MIERCOLES_ENTRADA] ?? null, $data[self::MIERCOLES_SALIDA] ?? null],
            [Carbon::THURSDAY, $data[self::JUEVES_ENTRADA] ?? null, $data[self::JUEVES_SALIDA] ?? null],
            [Carbon::FRIDAY, $data[self::VIERNES_ENTRADA] ?? null, $data[self::VIERNES_SALIDA] ?? null],
            [Carbon::SATURDAY, $data[self::SABADO_ENTRADA] ?? null, $data[self::SABADO_SALIDA] ?? null],
            [Carbon::SUNDAY, $data[self::DOMINGO_ENTRADA] ?? null, $data[self::DOMINGO_SALIDA] ?? null],
        ];

        foreach ($days as $day) {
            if (!is_null($day[1] ?? null)) {
                $date = Carbon::now()->previous($day[0])->toDateString();
                $startTime = $day[1];
                $endTime = $day[2];
                $event = [
                    "name" => "Disponible",
                    "start" => "$date $startTime",
                    "end" => "$date $endTime",
                    "color" => "#2196F3",
                    "timed" => true
                ];
                $this->command->info("Creating shift event " . json_encode($event) . " for user $user->email...");
                $shift->events()->create($event);
                $this->command->info("Shift event " . json_encode($event) . " created for user $user->email...");
            }
        }
    }

    private function userData($data)
    {
        $fullName = $data[self::EMPLEADO];
        $name = $this->stripAccents($fullName);
        $name = strtolower($name);
        $names = explode(", ", $name);
        $lastName = $names[0];
        $firstName = $names[1];
        $email = "$firstName.$lastName@misionesonline.net";
        return [
            "fullName" => $fullName,
            "email" => $email
        ];
    }

    function stripAccents($str)
    {
        return strtr(utf8_decode($str), utf8_decode('àáâãäçèéêëìíîïñòóôõöùúûüýÿÀÁÂÃÄÇÈÉÊËÌÍÎÏÑÒÓÔÕÖÙÚÛÜÝ'), 'aaaaaceeeeiiiinooooouuuuyyAAAAACEEEEIIIINOOOOOUUUUY');
    }


